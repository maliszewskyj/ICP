	FUNCTION ISTOP(ISTAT)
C       ***	ISTOP   = 0  ALL OK
C       = 1  IMMEDIATE STOP FROM CONTROL A
C       = 2  RUN ABORT WITH DATA SAVED (SET BY "STOP" PROG)
C       
C       ***	EVENT FLAG CLUSTER "INSTRU" CONTAINS FLAGS CORRESPONDING TO  
C       ISTOP   = 1  FLAG 63+NUMSTA(0) SET
C	ISTOP   = 2  FLAG 71+NUMSTA(0) SET
C	PAUSE FLAG   FLAG 79+NUMSTA(0) CLEARED
C       
C       ***	PAUSE FLAGS ARE SET BY "INTROD" AND CLEARED BY PROGRAM "PAUSE"
C       OR TYPING PAUS AT ? PROMPT
C	ISTOP WILL GO INTO TIMER HOLD AWAITING SETTING OF PAUSE EVT FLG BY PROG
C       RESUME
C	ISTOP PRINTS OUT MESSAGE GIVING TIME OF INITIATING AND TERMINATING PAUSE
C       
	use instr
	INTEGER*4    status,  READEF

	ISTOP=0
C	TEST CONTR_A AND IF SET THEN DON'T CLEAR
	status = READEF (contr_a)
	IF (status.ne.0)THEN
	   ISTOP=1
	   CALL CLREF(contr_a)
	   CALL PUTLOG(' IMMEDIATE STOP FROM CONTROL A')
	ENDIF
	RETURN
	END
