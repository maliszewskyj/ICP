	SUBROUTINE LOHI(IR)
	use fndlam

	IR=0
	DLR=1.
	R=3.14159/180.
	IJK=0
	ICOUNT(1,1)=-1
	CALL PUTLOG('***PRELIMINARY SCAN***')
 67	DO 70 K=1,KMAX,KMAX-1    
	   IJK=IJK+1
	   WRITE(UNIT=OUTBUF,FMT=102) IHKL(K)
	   CALL PUTLOG(OUTBUF)
	   DO 80 J=1,21      
	      CALL DRIVE(N,THEROX(K)-DLR+.1*DLR*J)      
	      CALL YIELD 
	      IF(ISTOP(0).NE.0)GO TO 300
	      IF(J-1) 51,52,51    
 52	      CALL RESET_DISPLAY
	      CALL CNTIME(15.)  
	      CALL YIELD 
	      CALL SCALER(COUNTS)   
	      IF(ISTOP(0).NE.0)GO TO 300
	      ICNT(IJK,J)=COUNTS(2)
C Rescale
	      WRITE(UNIT=OUTBUF,FMT=103)THEROX(K)-DLR+.1*DLR*J,COUNTS(2)/3 
	      CALL PUTLOG(OUTBUF)
	      GO TO 80   
 51	      IF (21-J) 53,52,53
 53	      CALL RESET_DISPLAY
	      CALL CNTIME(5.)  
	      CALL YIELD 
	      CALL SCALER(COUNTS)
	      WRITE(UNIT=OUTBUF,FMT=103)THEROX(K)-DLR+.1*DLR*J,COUNTS(2)
	      CALL PUTLOG(OUTBUF)
	      IF(ISTOP(0).NE.0)GO TO 300
	      ICNT(IJK,J)=COUNTS(2)
 80	   CONTINUE   
 900	   CALL SMOTH
C       DETERMINE MAX COUNT AND ANGLE   
	   IMAX(K)=ICNS(IJK,1) 
	   JMAX(K)=1  
	   IMIN(K)=ICNS(IJK,1) 
	   THEMAX(K)=(THEROX(K)-1.)/2.  
	   DO 82 J=2,21      
	      IF(IMAX(K)-ICNS(IJK,J)) 81,83,83  
 81	      IMAX(K)=ICNS(IJK,J) 
	      JMAX(K)=J  
	      THEMAX(K)=(THEROX(K)-1.+.1*(J-1))/2.    
	      GO TO 82
 83	      IF(IMIN(K)-ICNS(IJK,J)) 82,82,84  
 84	      IMIN(K)=ICNS(IJK,J) 
 82	   CONTINUE   
	   FI=(IMAX(K)-IMIN(K))*(IMAX(K)-IMIN(K))
	   FI2=IMIN(K)
	   IF(FI-9.*FI2)85,85,851
 85	   CALL PUTLOG(' PEAK MISSED')
 300	   IR=1
	   RETURN
 851	   IF(JMAX(K)-1) 85,85,852  
 852	   IF(JMAX(K)-21)70,85,85   
 70	CONTINUE   
	XI=XINC*300./(IMAX(1)-IMIN(1))
C       DETERMINE APPROX LAMDA AND ZCOR 
	DUM1=THEMAX(1)
	DUMMAX=THEMAX(KMAX)
	DO 30 I=1,10
	   THEMAX(1)=DUM1
	   THEMAX(KMAX)=DUMMAX
	   THEMAX(1)=THEMAX(1)-ZCOR/2.
	   THEMAX(KMAX)=THEMAX(KMAX)-ZCOR/2.
	   ALAMDA=2.*(TAN(THEMAX(1)*R)-TAN(THEMAX(KMAX)*R))*DSPACE(1)*DSPACE
	1	(KMAX)*COS(THEMAX(1)*PI)*COS(THEMAX(KMAX)*PI)/ (DSPACE(KMAX)*COS(
	2	THEMAX(KMAX)*PI) - DSPACE(1)*COS(THEMAX(1)*PI))    
	   DZCR=2.*ATAN(TAN(THEMAX(1)*R)-ALAMDA/2./DSPACE(1)/
	1	COS(THEMAX(1)*PI)) 
	   ZCOR=ZCOR+DZCR/PI
 30	CONTINUE
	ICOUNT(1,1)=3
 101	FORMAT(' FROM PRELIM SCAN LAMDA = ',F12.5,' ZCOR = ',F12.3)
 102	FORMAT(1x,'TWOTHETA  CTS/5SEC --- SCANNING ',I3,' PEAK')
 103	FORMAT(1x,F8.3,1x,I9)
	WRITE(UNIT=OUTBUF,FMT=101)ALAMDA,ZCOR
	CALL PUTLOG(OUTBUF)
	RETURN
	END
