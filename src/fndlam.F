      program fndlam
C
C     LEV 1 - 1401 - CHECK LAMDA  
C     MEASURES 5 PEAKS OF CU OR OTHER STANDARD AND LEAST SQUARE FITS      
C     LAMDA AND ZERO CORRECTION, GIVEN APPROX.LAMDA
C     PROG UPDATED 12/13/77  TO INCLUDE AL2O3 INSTEAD OF UNFILTERED SI
C     PROG UPDATED  1/ 4/78  TO INCLUDE BOTH AL2O3 AND UNFILTERED SI
C     PROG UPDATED  5/23/83  TO NEW FORMAT
C     PROG UPDATED  4/10/00  TO PORT TO LINUX - NCM
C     PROG UPDATED 11/15/11  TO FORTRAN 2003 - NCM
      use fndlam

C     INITIALIZE  
      IR=1
      CALL HDWINIT(IR) 
      CALL LOGFILEINIT
      R=3.14159/180.
 99   DO 1012 I=1,6
 1012    ISLGT(I)=0
      PI=3.14159/180.   
      IR=0
      IP=1
      DO 19 K=1,5
            IHKL(K)=K
            THETLO(K)=0
            THETHI(K)=0
            MINMAX(K)=0
            MIN(K)=0   
 19         MAX(K)=0   
 1    FORMAT(I1)
      CALL PUTLOG('TYPE 1 TO SKIP PRELIM SCAN')
      READ(*,1,ERR=2)IFSS1
      WRITE(UNIT=OUTBUF,FMT='(i5)')IFSS1
      CALL PUTLOG(OUTBUF)
      GO TO 3
 2    IFSS1=0
 3    CALL PUTLOG('TYPE 1 TO INCR STATISTICS')
      READ(*,1,ERR=4)IFSS2
      WRITE(UNIT=OUTBUF,FMT='(i5)')IFSS2
      CALL PUTLOG(OUTBUF)
      GO TO 7
 4    IFSS2=0
 7    CALL PUTLOG('TYPE 1 FOR MULT CALIBRTIONS(LAMPROX IGNORED) '//
     $     'ELSE TYPE 0')
      READ(*,1,ERR=1556)IFSS3
      WRITE(UNIT=OUTBUF,FMT='(i5)')IFSS3
      CALL PUTLOG(OUTBUF)
      GO TO 1557
 1556 IFSS3=0
 1557 CALL PUTLOG('WHAT POWDER? (0-CU,1-AL2O3,2-SI FILTERED,3-SI WITH'//
     $     ' 2 LAMDA,4-INPUT DS)-I1')
      READ(*,*) M
      WRITE(UNIT=OUTBUF,FMT='(i5)')M
      CALL PUTLOG(OUTBUF)
      IF (IFSS3.NE.0)GO TO 6
 5    CALL PUTLOG('TYPE LAMPROX')
      READ(*,*) ALAMDA
      WRITE(UNIT=OUTBUF,FMT='(f6.1)')ALAMDA
      CALL PUTLOG(OUTBUF)
 6    CONTINUE
      N=4
 8    CALL PUTLOG('TYPE 2THEMAX,ZERO CORRECTION')
      READ(*,*) TTHMAX,ZCOR0
      WRITE(UNIT=OUTBUF,FMT='(f6.2,1x,i5)')TTHMAX,ZCORO
      CALL PUTLOG(OUTBUF)
      IF(IFSS2.NE.0)GO TO 39
 17   CALL PUTLOG('ENTER SCALE FACTOR(1 = 5 SEC/POINT FIRST PEAK)')
      READ(*,*) IP
      WRITE(UNIT=OUTBUF,FMT='(i5)')IP
      CALL PUTLOG(OUTBUF)
 39   CONTINUE
      IF(IP.EQ.0)IP=1
      FMON=5.*IP
      GO TO (20,30,37,38,41),M+1     
 20   DSPACE(1) = 3.6147/SQRT(3.0)    
      DSPACE(2) = 3.6147/2.0   
      DSPACE(3) = 3.6147/SQRT(8.0)    
      DSPACE(4) = 3.6147/SQRT(11.)   
      DSPACE(5) = 3.6147/4.   
      IHKL(1)=111
      IHKL(2)=200
      IHKL(3)=220
      IHKL(4)=311
      IHKL(5)=400
      GO TO 32   
 30   DSPACE(1)=3.4800
      DSPACE(2)=2.5509
      DSPACE(3)=2.0854
      DSPACE(4)=1.7400
      DSPACE(5)=1.6014
      IHKL(1)=012
      IHKL(2)=104
      IHKL(3)=113
      IHKL(4)=024
      IHKL(5)=116
      GO TO 32
 37   DSPACE(1)=5.43088/SQRT(3.)
      DSPACE(2)=5.43088/SQRT(8.)
      DSPACE(3)=5.43088/SQRT(11.)
      DSPACE(4)=5.43088/SQRT(19.)
      DSPACE(5)=5.43088/SQRT(24.)
      IHKL(1)=111
      IHKL(2)=220
      IHKL(3)=113
      IHKL(4)=331
      IHKL(5)=224
      GO TO 32
 38   DSPACE(1)=5.43088/SQRT(3.)
      DSPACE(2)=5.43088/SQRT(8.)
      DSPACE(3)=5.43088/SQRT(19.)
      DSPACE(4)=5.43088/SQRT(24.)
      DSPACE(5)=5.43088/SQRT(32.)
      IHKL(1)=111
      IHKL(2)=220
      IHKL(3)=331
      IHKL(4)=224
      IHKL(5)=440
      GO TO 32
 41   CALL PUTLOG('TYPE NO. AND VALUES OF D-S,GIVE ONLY 5 VALUES')
      READ(*,*) KMAX,DSPACE(1),DSPACE(2),DSPACE(3),DSPACE(4),DSPACE(5)
      IF(KMAX.GT.5)KMAX=5
 410  CALL PUTLOG('TYPE PEAK SCALE FACTORS (5I5) DEFAULT=10,10,10,10,10,')
      READ(*,*) JMAX(1),JMAX(2),JMAX(3),JMAX(4),JMAX(5)
      IF (JMAX(1).LT.1) GO TO 33
      DO 36 K=1,5
 36      ISCALE(K,5)=JMAX(K)
      GO TO 33   
 32   KMAX=5     
 33   CALL PUTLOG('TYPE COLLIMATIONS 1,2,3,MONO.MOSAIC(ALL MIN.),DMONO')
      READ(*,*) A1,A2,A3,BE,DMONO
      IF (IFSS2.EQ.0)GO TO 1000
      XINC=200
      CALL PUTLOG(' TYPE IN NO. OF COUNTS ABOVE BKGD DESIRED IN PEAKS')
      READ(*,*) XINC
      IF(XINC.LE..1)XINC=200.
 1000 CONTINUE
      ANGMON=ASIN(ALAMDA/2./DMONO)/R
      A1=(A1/60.)**2
      A2=(A2/60.)**2
      A3=(A3/60.)**2
      BE=(BE/60.)**2
      DENOM=A1+A2+4.*BE
      FAC1=A1*A2+A2*A3+A1*A3+4.*BE*(A2+A3)
      FAC2=4.*A2*(A1+2.*BE)
      FAC3=4.*(A1*A2+BE*A1+BE*A2)
      IF(IFSS3.EQ.0)GO TO 55
 510  CALL PUTLOG('TYPE DRUM MIN, MAX, STEP, ZERO,  MONO ZERO')
      READ(*,*) DRMIN,DRMAX,DRSTP,DRZER,XTZER
      IF ((DRMAX-DRMIN).LT.0.) GO TO 99
      NLOOP=(DRMAX-DRMIN+DRSTP/5.)+1
      XTSTP=DRSTP/2.
      ANGMON=(DRMIN-DRZER)/2.-XTSTP
      DO 59 NPASS=1,NLOOP
         FMON=5.*IP
         ANGMON=ANGMON+XTSTP
         ALAMDA=2.*DMONO*SIN(ANGMON*R)
         TRGXT=ANGMON+XTZER
         TRGDR=2.*ANGMON+DRZER
         IF(TRGDR.LT.DRMIN.OR.TRGDR.GT.DRMAX) GO TO 59
         WRITE(UNIT=OUTBUF,FMT=5101) TRGXT, TRGDR
         CALL PUTLOG(OUTBUF)
 5101    FORMAT('DRIVING A1=',F8.3,' A2=',F8.3)
         CALL DRIVE(2,TRGDR)
         CALL DRIVE(1,TRGXT)
         CALL YIELD
         XMTIM=10.
         IF(ISTOP(0).NE.0) GO TO 99
 55      ZCOR=ZCOR0
         CALL PEAKPO(IR)
         IF(IR.EQ.1)GO TO 99
 631     IF (IFSS1) 66,67,66 
 67      CALL LOHI(IR)
         IF(IR.EQ.1)GO TO 99
 66      DO 111 K=1,KMAX   
            THEROX(K)=2.*ASIN(ALAMDA/(2.*DSPACE(K)))/R+ZCOR    
            RAT=TAN(R*THEROX(K)/2.)/TAN(R*ANGMON)
            DL(K)=0.2*SQRT((FAC1-FAC2*RAT+FAC3*RAT*RAT)/DENOM)
            IF(TTHMAX-(THEROX(K)+6.*DL(K))) 110,111,111 
 110        KMAX=K-1   
            GO TO 112  
 111     CONTINUE   
 112     CALL SCALE(IR)
         IF(IR.EQ.1)GO TO 99
         CALL CNT(IR)
         IF(IR.EQ.1)GO TO 99
 710     DO 720 K=1,KMAX   
C     The following lines seem unnecessary - NCM Apr 2000
C            CALL CNTIME(6.)
C            CALL YIELD
C            CALL SCALER(COUNTS)
            IMAX(K)=IMAX(K)/(FMONIT(K)/300.)    
 720     CONTINUE   
         WRITE(UNIT=OUTBUF,FMT=13)(IHKL(I),I=1,KMAX)
         CALL PUTLOG(OUTBUF)
 13      FORMAT('H K L',13X,5(2X,I3,2X))
         WRITE (UNIT=OUTBUF,FMT=10)(DSPACE(K),K=1,KMAX)
         CALL PUTLOG(OUTBUF)
 10      FORMAT('DSPACINGS',9X,5F7.4,1X,'AU')   
         WRITE (UNIT=OUTBUF,FMT=12) (IMAX(K), K=1,KMAX) 
         CALL PUTLOG(OUTBUF)
 11      FORMAT('PEAK 2THETA@S ARE',1X,5F7.3,1X,'DEG') 
         WRITE(UNIT=OUTBUF,FMT=11) (THEMAX(K), K= 1,KMAX)   
         CALL PUTLOG(OUTBUF)
 12      FORMAT ('PEAK HEIGHTS ARE',2X,5F7.0,1X,'COUNTS/5SEC')  
         DO 2001 K=1,KMAX  
            THEMAX(K)=THEMAX(K)/2. 
 2001    CONTINUE   
         ZCOR=ZCOR/2.
         CALL LAMZER
         IF(IFSS3.EQ.0)GO TO 60
         CALC=ASIN(ALAMDA/2./DMONO)/R
         CRRMON=TRGDR-2.*CALC
         WRITE (UNIT=OUTBUF,FMT=57)CRRMON,TRGDR
         CALL PUTLOG(OUTBUF)
 57      FORMAT('CALC DRUM ZERO = ',F9.3,' DRUM SETTING = ',F9.3)
 59   CONTINUE
      STOP
 2002 CALL PUTLOG('BAD FORMAT')
      GO TO 99
 60   CALL EXIT
      END
