	SUBROUTINE CNT(IR)
C       MEASURES 5 PEAKS OF CU OR OTHER STANDARD AND LEAST SQUARE FITS      
C       LAMDA AND ZERO CORRECTION, GIVEN APPROX.LAMDA
C
C       UPDATED APRIL 2000 TO USE INCLUDED COMMON
	use fndlam

	IR=0
	R=3.14159/180.
	IF(IFSS2.EQ.0)XI=60.*FMON
	TIMJ=0.
C	Changed 6/95 JWL (KK)
	DO 1210 KK=KLO,KHI,KSTP
	   FGH=(XI*ISCALE(KK,M+1))/10.
	   IF(FGH.LT.300.)FGH=300.
 1210	TIMJ=TIMJ+FGH*13.
	WRITE(UNIT=OUTBUF,FMT=1211) TIMJ/3600.
	CALL PUTLOG(OUTBUF)
 1211	FORMAT(' EST.TIME FOR RUN =',F10.2,' MIN.')
 123	DO 210 K=KLO,KHI,KSTP    
 1312	   FMONIT(K)=(XI*ISCALE(K,M+1))/10   
	   IF(FMONIT(K)-300.) 1313,1314,1314 
 1313	   FMONIT(K)=300.*FMON/5.      
 1314	   FMON=FMONIT(K)/60.   
	   WRITE(UNIT=OUTBUF,FMT=997) IHKL(K), FMON
	   CALL PUTLOG(OUTBUF)
 1311	   DO 130 J=1,13     
	      CALL DRIVE(N,THEROX(K)-(7-J)*DL(K))  
	      CALL YIELD 
	      IF(ISTOP(0).NE.0)GO TO 300
	      IF(J-1) 135,131,135 
 131	      CALL RESET_DISPLAY
	      CALL CNTIME(FMON)
	      CALL SCALER(COUNTS)
	      IF(ISTOP(0).NE.0)GO TO 300
	      ICOUNT(K,J)=COUNTS(2)
	      IF(IR.NE.0)GO TO 300
	      ANGL(K,J) = THEROX(K)-(7-J)*DL(K)
C	Changed 6/95 JWL     
	      WRITE(UNIT=OUTBUF,FMT=998) ANGL(K,J),ICOUNT(K,J)
	      CALL PUTLOG(OUTBUF)
 997	      FORMAT(1x,'TWOTHETA    COUNTS --- SCANNING ',I3,' PEAK ',
	1	   F8.1,' SEC/PT')
 998	      FORMAT(1x,F8.3,1x,F9.0)
	      GO TO 130  
 135	      IF(13-J) 132,131,132     
 132	      CALL RESET_DISPLAY
	      CALL CNTIME(FMON)
	      CALL SCALER(COUNTS)
	      IF(ISTOP(0).NE.0)GO TO 300
	      ICOUNT(K,J)=COUNTS(2)
	      IF(IR.NE.0)GO TO 300
	      ANGL(K,J) = THEROX(K)-(7-J)*DL(K)     
	      WRITE(UNIT=OUTBUF,FMT=998) ANGL(K,J),ICOUNT(K,J)
	      CALL PUTLOG(OUTBUF)
 130	   CONTINUE   
 1231	   CALL SMOTH
	   CALL CNAN
	   IF (ISLGT(K)) 155,1601,155      
 1601	   ISLGT(K)=1
C       CENTER PEAK
	   IF(JMAX(K)-6) 1501,155,1502    
 1502	   IF(JMAX(K)-8) 155,155,1503      
 1501	   THEROX(K)=ANGL(K,JMAX(K))     
	   DO 1510 J=13,8-JMAX(K),-1
	      ICOUNT(K,J)=ICOUNT(K,J-(7-JMAX(K)))    
	      ANGL(K,J)=ANGL(K,J+JMAX(K)-7)   
 1510	   CONTINUE   
	   ICOUNT(K,13)=ICOUNT(K,13)     
	   DO 1520 J=1,7-JMAX(K)    
	      CALL DRIVE(N,THEROX(K)-(7-J)*DL(K))  
	      CALL YIELD 
	      IF(ISTOP(0).NE.0)GO TO 300
	      IF(J-1) 1511,1511,1512     
 1511	      CALL RESET_DISPLAY
	      CALL CNTIME(FMON)
	      CALL SCALER(COUNTS)
	      IF(ISTOP(0).NE.0)GO TO 300
	      ICOUNT(K,J)=COUNTS(2)
	      IF(IR.NE.0)GO TO 300
	      GO TO 1513 
 1512	      CALL RESET_DISPLAY
	      CALL CNTIME(FMON)
	      CALL SCALER(COUNTS)
	      IF(ISTOP(0).NE.0)GO TO 300
	      ICOUNT(K,J)=COUNTS(2)
	      IF(IR.NE.0)GO TO 300
 1513	      CONTINUE
C       CHANGED APR 9 RWE
	      ANGL(K,J)=THEROX(K)-(7-J)*DL(K)
C       ANGL(K,J)=ANGL(K,J+JMAX(K)-7)
 1520	   CONTINUE   
	   GO TO 1231 
 1503	   THEROX(K)=ANGL(K,JMAX(K))     
	   DO 1530 J=1,20-JMAX(K)   
	      ICOUNT(K,J)=ICOUNT(K,J+JMAX(K)-7)      
	      ANGL(K,J)=ANGL(K,J+JMAX(K)-7)   
 1530	   CONTINUE   
	   ICOUNT(K,1)=ICOUNT(K,1)
	   DO 1540 J=21-JMAX(K),13  
	      CALL DRIVE(N,THEROX(K)-(7-J)*DL(K))  
	      CALL YIELD 
	      IF(ISTOP(0).NE.0)GO TO 300
	      IF(13-J) 1531,1532,1531  
 1532	      CALL RESET_DISPLAY
	      CALL CNTIME(FMON)
	      CALL SCALER(COUNTS)
	      IF(ISTOP(0).NE.0)GO TO 300
	      ICOUNT(K,J)=COUNTS(2)
	      IF(IR.NE.0)GO TO 300
	      GO TO 1533
 1531	      CALL RESET_DISPLAY
	      CALL CNTIME(FMON)
	      CALL SCALER(COUNTS)
	      IF(ISTOP(0).NE.0)GO TO 300
	      ICOUNT(K,J)=COUNTS(2)
	      IF(IR.NE.0)GO TO 300
 1533	      CONTINUE
C       CHANGED APR 9 RWE
	      ANGL(K,J)=THEROX(K)-(7-J)*DL(K)
C       ANGL(K,J)=ANGL(K,J+JMAX(K)-7)   
 1540	   CONTINUE   
	   GO TO 1231 
 155	   IF(JMAX(K).EQ.1.OR.JMAX(K).EQ.13)GO TO 199
	   DO 160 J=JMAX(K),1,-1    
	      IF (ICOUNS(K,J)-(IMAX(K)+IMIN(K))/2)161,162,160      
 162	      THETLO(K)=ANGL(K,J)      
	      GO TO 170  
 161	      THETLO(K)=(((IMAX(K)+IMIN(K))/2-ICOUNS(K,J))*DL(K))/(ICOUNS(K,J+1) 
	1	   -ICOUNS(K,J))+ANGL(K,J)  
	      GO TO 170  
 160	   CONTINUE   
	   GO TO 1701 
 170	   MIN(K)=1   
 1701	   DO 180 J=JMAX(K),13      
	      IF(ICOUNS(K,J)-(IMAX(K)+IMIN(K))/2) 171,172,180      
 172	      THETHI(K)=ANGL(K,J)      
	      GO TO 190  
 171	      THETHI(K)=ANGL(K,J)-(((IMAX(K)+IMIN(K))/2-ICOUNS(K,J))*DL(K))      
	1	   /(ICOUNS(K,J-1)-ICOUNS(K,J))
	      GO TO 190
 180	   CONTINUE   
	   GO TO 1901 
 190	   MAX(K)=1   
 1901	   MINMAX(K)=MIN(K)+MAX(K)  
	   GO TO (199,200,201),MINMAX(K)+1 
 199	   CALL PUTLOG(' PEAK MISSED')
 300	   IR=1
	   RETURN
 201	   THEMAX(K)=(THETLO(K)+THETHI(K))/2.    
	   WRITE(UNIT=OUTBUF,FMT=621)
	   CALL PUTLOG(OUTBUF)
 200	   DO 220 J=1,13     
	      WRITE(UNIT=OUTBUF,FMT=62)ANGL(K,J),ICOUNT(K,J),ICOUNS(K,J)
	      CALL PUTLOG(OUTBUF)
 62	      FORMAT(1x,F8.3,1X,F9.0,1x,F9.0)
 621	      FORMAT(1x,'TWOTHETA    COUNTS  SMCOUNTS')
	      CALL YIELD 
 220	   CONTINUE   
	   WRITE(UNIT=OUTBUF,FMT=63)
	   CALL PUTLOG(OUTBUF)
	   WRITE(UNIT=OUTBUF,FMT=6311)
	   CALL PUTLOG(OUTBUF)
	   WRITE(UNIT=OUTBUF,FMT=6310)MIN(K),MAX(K),THETLO(K),THETHI(K)  
	   CALL PUTLOG(OUTBUF)
 6310	   FORMAT(2I4,2(1X,F8.3))   
 6311	   FORMAT(' MIN MAX 2THETALO 2THETAHI')
	   WRITE(UNIT=OUTBUF,FMT=63)
	   CALL PUTLOG(OUTBUF)
 210	CONTINUE   
 63	FORMAT(///)
	RETURN
	END
