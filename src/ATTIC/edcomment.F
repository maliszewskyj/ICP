#include "icp.h"
	include COMPILE_OPTS
	SUBROUTINE EdComment(VID,KID,ROW,COL,TRM,INSERT)
C	************************************************

C++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++C
C                                                                              C
C	EdComment: EDIT SAMPLE LABEL IN SETUP AND COMMAND ROUTINES             C
C	GAYLE GREENE, MARCH 1994                                               C
C	MODIFIED FOR SANS PROGRAM, AUGUST 1994 (SK)                            C
C                                                                              C
C++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++C

	IMPLICIT NONE

	INTEGER*4 STATUS,VID,KID,ROW,COL,NEWROW,NEWCOL,MODIFIERS,TRM
	INTEGER*4 SMG$SET_CURSOR_REL,TERMINATOR
	INTEGER*4 SMG$READ_KEYSTROKE,SMG$RETURN_CURSOR_POS
	CHARACTER*1 CHAR_STRING
	LOGICAL*1 INSERT
	INCLUDE '($SMGDEF)'
	INCLUDE '($TRMDEF)'


	CALL Cursor_Abs(VID,ROW,COL)
	STATUS=SMG$READ_KEYSTROKE(KID,TERMINATOR)

	DO WHILE ((STATUS) .AND.
     1           (TERMINATOR .NE. SMG$K_TRM_CR)
     1	         .AND.
     1           (TERMINATOR .NE. SMG$K_TRM_DOWN)
     1	         .AND.
     1           (TERMINATOR .NE. SMG$K_TRM_UP)
     1	         .AND.
c     1           (TERMINATOR .NE. SMG$K_TRM_F7)
c     1	         .AND.
c     1           (TERMINATOR .NE. SMG$K_TRM_F8)
c     1	         .AND.
c     1           (TERMINATOR .NE. SMG$K_TRM_F9)
c     1	         .AND.
c     1           (TERMINATOR .NE. SMG$K_TRM_F10)
c     1	         .AND.
c     1           (TERMINATOR .NE. SMG$K_TRM_F11)
c     1	         .AND.
c     1           (TERMINATOR .NE. SMG$K_TRM_F12)
c     1	         .AND.
     1           (TERMINATOR .NE. SMG$K_TRM_F13)
     1	         .AND.
     1           (TERMINATOR .NE. SMG$K_TRM_F14)
     1	         .AND.
c     1           (TERMINATOR .NE. SMG$K_TRM_F15)
c     1	         .AND.
     1           (TERMINATOR .NE. SMG$K_TRM_F16)
     1	         .AND.
     1           (TERMINATOR .NE. SMG$K_TRM_F17)
     1	         .AND.
     1           (TERMINATOR .NE. SMG$K_TRM_F18)
     1	         .AND.
     1           (TERMINATOR .NE. SMG$K_TRM_F19)
     1	         .AND.
     1           (TERMINATOR .NE. SMG$K_TRM_F20)
     1	         .AND.
     1           (TERMINATOR .NE. SMG$K_TRM_ENTER))

C   CHECK STATUS OF LAST READ
	IF (.NOT.STATUS)CALL LIB$SIGNAL(%VAL(STATUS))

D	WRITE(8,1)TERMINATOR
D1	FORMAT(' TERMINATOR IS: ',I5)

C  RIGHT
	IF (TERMINATOR.EQ.SMG$K_TRM_RIGHT) THEN
	   CALL SMG$RETURN_CURSOR_POS(VID,NEWROW,NEWCOL)
	   IF (NEWCOL .LT. (COL+60))THEN
		CALL SMG$SET_CURSOR_REL(VID,0,1)
	   ELSE
		CALL Cursor_Abs(VID,ROW,COL)
	   ENDIF

C  LEFT
	ELSE IF (TERMINATOR.EQ.SMG$K_TRM_LEFT) THEN
	   CALL SMG$RETURN_CURSOR_POS(VID,NEWROW,NEWCOL)
	   IF (NEWCOL .GT. COL)THEN
		CALL SMG$SET_CURSOR_REL(VID,0,-1)
	   ELSE
		CALL Cursor_Abs(VID,ROW,COL+60)
	   ENDIF

C  TAB
	ELSE IF(TERMINATOR.EQ.SMG$K_TRM_HT) THEN
	  CALL SMG$RETURN_CURSOR_POS(VID,NEWROW,NEWCOL)
	  IF (NEWCOL .LT. (COL+51))THEN
	    CALL SMG$SET_CURSOR_REL(VID,0,+10)
	  ELSE IF(NEWCOL .EQ. (COL+60)) THEN
	    CALL Cursor_Abs(VID,ROW,COL)
	  ELSE
	    CALL Cursor_Abs(VID,ROW,COL+60)
	  ENDIF

C  DELETE
	ELSE IF (TERMINATOR.EQ.SMG$K_TRM_DELETE) THEN
	   CALL SMG$RETURN_CURSOR_POS(VID,NEWROW,NEWCOL)
	   IF (NEWCOL .GT. COL)THEN
		CALL SMG$SET_CURSOR_REL(VID,0,-1)
		CALL SMG$DELETE_CHARS(VID,1,NEWROW,NEWCOL-1)
	   ENDIF

C  REMOVE
	ELSE IF (TERMINATOR.EQ.SMG$K_TRM_REMOVE) THEN
	   CALL SMG$RETURN_CURSOR_POS(VID,NEWROW,NEWCOL)
	   CALL SMG$DELETE_CHARS(VID,1,NEWROW,NEWCOL)

C  SPACE
	ELSE IF(TERMINATOR .EQ. SMG$K_TRM_SPACE)THEN
	   CALL SMG$RETURN_CURSOR_POS(VID,NEWROW,NEWCOL)
	   IF(INSERT)THEN
		CALL SMG$INSERT_CHARS(VID,' ',NEWROW,NEWCOL,SMG$M_REVERSE)
	   ELSE IF(.NOT. INSERT)THEN
		CALL SMG$PUT_CHARS(VID,' ',NEWROW,NEWCOL,,SMG$M_REVERSE)	
	   END IF

C  A NUMBER OR LETTER 
	ELSE IF(TERMINATOR .GT. 32 .AND. TERMINATOR .LT. 127)THEN
	   CHAR_STRING = CHAR(TERMINATOR)
	   CALL SMG$RETURN_CURSOR_POS(VID,NEWROW,NEWCOL)
	   IF(INSERT)THEN
		CALL SMG$INSERT_CHARS(VID,CHAR_STRING,NEWROW,NEWCOL,
     1					SMG$M_REVERSE)
	   ELSE IF (.NOT. INSERT)THEN
		CALL SMG$PUT_CHARS(VID,CHAR_STRING,NEWROW,NEWCOL,,
     1					SMG$M_REVERSE)
	   END IF

C  PART OF THE EXTENDED CHARACTER SET
	ELSE IF(TERMINATOR .GT. 127 .AND. TERMINATOR .LT. 255)THEN
	   CHAR_STRING = CHAR(TERMINATOR)
	   CALL SMG$RETURN_CURSOR_POS(VID,NEWROW,NEWCOL)
	   IF(INSERT)THEN
		CALL SMG$INSERT_CHARS(VID,CHAR_STRING,NEWROW,NEWCOL,
     1					SMG$M_REVERSE)
	   ELSE IF (.NOT. INSERT)THEN
		CALL SMG$PUT_CHARS(VID,CHAR_STRING,NEWROW,NEWCOL,,
     1					SMG$M_REVERSE)
	   END IF
	END IF

C  READ ANOTHER STRING
	STATUS=SMG$READ_KEYSTROKE(KID,TERMINATOR)
	END DO

C  VALID TERMINATOR FOUND: USER FINISHED WITH COMMENT
	TRM = TERMINATOR
D	WRITE(8,2)TRM
D2	FORMAT(' TRM IS: ',I5)

	RETURN
	END
