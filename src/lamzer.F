	SUBROUTINE LAMZER
	USE fndlam
	REAL*4 R, DELTHE(5),THERIM(5)
	REAL*4 SUMDES, SUMA, SUMB, DEZCOR, ERRLAM, ERZCOR

	R=3.14159/180.
C       DETERMINE LAMDA AND ZERO CORRECTION     
 3000	DO 350 L=1,10     
	   SUMDES=0  
	   SUMA=0     
	   SUMB=0     
	   SUMC=0     
	   SUMD=0     
	   DO 300 K=1,KMAX   
	      THEROX(K)=ASIN(ALAMDA/(2.*DSPACE(K)))/R + ZCOR
	      DELTHE(K)=(THEMAX(K) - THEROX(K))/(180./3.14159) 
	      SUMDES=SUMDES + DELTHE(K)*DELTHE(K)
	      THERIM(K)=(1./(2.*DSPACE(K)))/SQRT(1.-(ALAMDA*ALAMDA)/    
	1	   (4*DSPACE(K)*DSPACE(K)))
	      SUMA=SUMA + DELTHE(K)*THERIM(K)     
	      SUMB=SUMB + DELTHE(K)   
	      SUMC=SUMC + THERIM(K)  
	      SUMD=SUMD +THERIM(K)*THERIM(K)     
 300	   CONTINUE   
	   DET=KMAX*SUMD - SUMC*SUMC
	   DELAM=(SUMA*KMAX - SUMC*SUMB)/DET      
	   DEZCOR=((SUMD*SUMB - SUMC*SUMA)/DET)*(180./3.14159) 
	   ALAMDA=ALAMDA + DELAM    
	   ZCOR=ZCOR + DEZCOR      
 350	CONTINUE   
	DO 35 K=1,KMAX
 35	   THEROX(K)=2.*ASIN(ALAMDA/(2.*DSPACE(K)))/R+ZCOR*2.
	   WRITE (UNIT=OUTBUF,FMT=11)(THEROX(K),K=1,KMAX)
	   CALL PUTLOG(OUTBUF)
 11	   FORMAT('CALC 2THETA@S ARE',1X,5F7.3,1X,'DEG')
	   ZCOR=2.*ZCOR      
	   IF(KMAX-2) 500,500,502   
 500	   WRITE(UNIT=OUTBUF,FMT=560) ALAMDA   
	   CALL PUTLOG(OUTBUF)
 560	   FORMAT('LAMDA AU =',F8.4,'+-INFINITY') 
	   WRITE(UNIT=OUTBUF,FMT=570)ZCOR     
	   CALL PUTLOG(OUTBUF)
 570	   FORMAT('2THETA ZERO CORR-ADD TO CALC VALUE=',F8.4,'+-INFINITY')    
	   RETURN
 502	   ERRLAM=.6745*SQRT((KMAX*SUMDES)/((KMAX-2)*ABS(DET)))
	   ERZCOR=.6745*(360./3.14159)*SQRT((SUMD*SUMDES)/((KMAX-2)*ABS     
	1	(DET)))   
	   ERZCOR=ERZCOR*2.
	   ENERGY=81.7964/(ALAMDA**2)
	   ERRENG=ENERGY - 81.7964/((ALAMDA+ERRLAM)**2)
	   WRITE(UNIT=OUTBUF,FMT=3) ALAMDA, ERRLAM, ENERGY, ERRENG   
	   CALL PUTLOG(OUTBUF)
 3	   FORMAT('LAMDA AU       = ',F8.4,'+-',F10.5/'INCIDENT ENERGY = ',
	1	F8.3,' +-',F8.5)   
	   WRITE (UNIT=OUTBUF,FMT=4) ZCOR, ERZCOR    
	   CALL PUTLOG(OUTBUF)
 4	   FORMAT('2THETA ZERO CORR-ADD TO CALC=',F8.4,'+-',E10.4,'DEG')      
	   RETURN
	   END
