	SUBROUTINE PEAKPO(IR)
C       LEV 1 - 1401 - CHECK LAMDA  
C       MEASURES 5 PEAKS OF CU OR OTHER STANDARD AND LEAST SQUARE FITS      
C       LAMDA AND ZERO CORRECTION, GIVEN APPROX.LAMDA
	use fndlam

C       CALCULATE APPROX PEAK POS AND SCAN LO AND HI PEAKS   
	IR=0
	R=3.14159/180.
	WRITE(UNIT=OUTBUF,FMT=101) 
	CALL PUTLOG(OUTBUF)
	IF (ALAMDA-2.*DSPACE(1)) 652,621,621
 652	DO 65 K=1,KMAX    
	   IF(ALAMDA-2.*DSPACE(K)) 651,60,60      
 651	   THEROX(K)=2.*ASIN(ALAMDA/(2.*DSPACE(K)))/R+ZCOR
	   WRITE(UNIT=OUTBUF,FMT=102) IHKL(K), THEROX(K)
	   CALL PUTLOG(OUTBUF)
	   FAK=THEROX(K)+1.
	   IF(TTHMAX-FAK) 60,65,65   
 65	CONTINUE   
	GO TO 601  
 60	KMAX=K-1   
 601	IF (KMAX-2) 621,631,631  
 621	PRINT *,' LAMDA TOO LARGE'
	IR=1
	GO TO 641
 631	WRITE(UNIT=OUTBUF,fmt='(1x,a,i4)') 'NO. OF PEAKS IN 2TH RANGE=',KMAX
	CALL PUTLOG(OUTBUF)
 641	RETURN
 101	FORMAT(1X,'HKL',1X,'TWOTHETA')
 102	FORMAT(1X,I3,1X,F8.3)
	END
